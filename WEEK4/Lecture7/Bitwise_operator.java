package WEEK4.Lecture7;

public class Bitwise_operator {
    public static void main(String[] args) {
        System.out.println(2 & 3);
        System.out.println(2 | 3);
        System.out.println(2 ^ 3);
        System.out.println(~2);
        System.out.println(34 << 1);
        System.out.println(34 >> 2);
        System.out.println(-4 << 1);
        System.out.println(-4 >> 1);
    }
    
}
